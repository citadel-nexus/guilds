class CSharpGenerator:
    """
    Translates validated .sake JSON IR into C# classes.
    Targets UnrealCLR / .NET 8.
    """

    # Type mapping: Python → C#
    TYPE_MAP = {
        "str": "string",
        "int": "int",
        "float": "double",
        "bool": "bool",
        "list": "List<object>",
        "dict": "Dictionary<string, object>",
        "None": "void",
        "Optional[str]": "string?",
        "Optional[int]": "int?",
        "Optional[float]": "double?",
    }

    def __init__(self):
        self.logger = logging.getLogger("CSharpGenerator")

    def translate(self, sake: SakeFile) -> str:
        """Generate complete C# class from .sake file."""
        task = sake.taskir_blocks
        layers = sake.sake_layers
        backend = layers.backend_layer
        meta = sake.metadata

        class_name = self._to_pascal_case(task.task_name)
        namespace = f"CitadelNexus.{backend.framework}"

        lines = []
        lines.append(f"// Auto-generated by F993 Backend Translator")
        lines.append(f"// SRS: {meta.srs_code} | Task: {task.task_id}")
        lines.append(f"// Archetype: {layers.archetype_layer.value}")
        lines.append(
            f"// CAPS Trust: {layers.caps_profile.trust_score}"
        )
        lines.append(f"// Generated: {time.strftime('%Y-%m-%d %H:%M:%S')}")
        lines.append("")
        lines.append("using System;")
        lines.append("using System.Collections.Generic;")
        lines.append("using System.Threading.Tasks;")
        if "UnrealCLR" in backend.framework:
            lines.append("using UnrealEngine.Framework;")
        lines.append("")
        lines.append(f"namespace {namespace}")
        lines.append("{")

        # CAPS compliance attribute
        lines.append("    /// <summary>")
        lines.append(f"    /// {task.description}")
        lines.append("    /// </summary>")
        lines.append(
            f"    [CAPSCompliance("
            f"TrustScore = {layers.caps_profile.trust_score}, "
            f"Archetype = \"{layers.archetype_layer.value}\")]"
        )
        lines.append(f"    public class {class_name}")
        lines.append("    {")

        # Properties from inputs
        for inp in task.inputs:
            csharp_type = self.TYPE_MAP.get(
                inp.get("type", "str"), "object"
            )
            prop_name = self._to_pascal_case(inp.get("name", "input"))
            lines.append(
                f"        public {csharp_type} {prop_name} "
                f" get; set; "
            )
        lines.append("")

        # Constructor
        lines.append(f"        public {class_name}()")
        lines.append("        {")
        lines.append(
            f"            // SRS: {meta.srs_code} initialization"
        )
        lines.append("        }")
        lines.append("")

        # Main execution method
        return_type = "object"
        if task.outputs:
            out_type = task.outputs[0].get("type", "object")
            return_type = self.TYPE_MAP.get(out_type, "object")

        lines.append(
            f"        public async Task<{return_type}> ExecuteAsync()"
        )
        lines.append("        {")

        # Pseudocode as comments
        if task.pseudocode:
            for pseudo_line in task.pseudocode.split("\n"):
                lines.append(f"            // {pseudo_line.strip()}")
            lines.append("")

        # FATE logging
        lines.append(
            "            // FATE: Log execution start"
        )
        lines.append(
            "            await FATELedger.LogAsync("
            f"\"{meta.srs_code}\", "
            f"\"EXECUTE\", "
            f"{layers.fate_profile.tp_cost});"
        )
        lines.append("")
        lines.append(
            f"            throw new NotImplementedException("
            f"\"Implement {task.task_name} logic\");"
        )
        lines.append("        }")

        # Validation method
        lines.append("")
        lines.append("        public bool ValidateCAPS()")
        lines.append("        {")
        lines.append(
            f"            return CAPSScorer.Validate("
            f"\"{meta.srs_code}\", "
            f"{layers.caps_profile.trust_score});"
        )
        lines.append("        }")

        lines.append("    }")
        lines.append("}")

        return "\n".join(lines)

    def _to_pascal_case(self, name: str) -> str:
        return "".join(
            word.capitalize()
            for word in name.replace("-", "_").split("_")
        )

