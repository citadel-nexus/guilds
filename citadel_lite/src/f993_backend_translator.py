class SakeMetadata(BaseModel):
    srs_code: str  # F\d{3} format
    generator: str = "F993_backend_translator"
    timestamp: float = Field(default_factory=time.time)
    reflex_group: str = ""
    code_gen_hooks: List[str] = []
    ledger_hooks: List[str] = []
    provenance: Dict[str, Any] = {}

    @validator("srs_code")
    def validate_srs(cls, v):
        import re
        if not re.match(r"^F\d{3}$", v):
            raise ValueError(f"SRS code must match F\\dhttps://www.notion.so/c4053b189184403d9a8de1a47b0f0a8c: {v}")
        return v


